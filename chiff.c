void KeyExpansion(unsigned char *key, unsigned char **w,int Nk,int Nb,int Nr)
{
  unsigned char *temp = calloc(4,sizeof(unsigned char));

  int i = 0;

  while (i < Nk)
  {
    for (int j = 0; j < 4; j = j + 1)
      w[i][j] = key[4 * i + j];
      
    i = i + 1;
  }

  i = Nk;
  int bo = Nb * (Nr + 1);
  while (i < bo)
  {
	for (int k = 0; k < 4; k = k + 1)
	{
	  temp[k] = w[i - 1][k];
	} 
    if ((i % Nk) == 0)
    {
      RotWord(temp);
      SubWord(temp);
      temp[0] = temp[0] ^ RCON[(i/Nk) - 1 %51];
    }
    else if (Nk > 6 && (i % Nk) == 4)
    {
      SubWord(temp);
    }
    for (int j = 0; j < 4; j = j + 1)
    { 
      w[i][j] = w[i - Nk][j] ^ temp[j];
    }
    i = i + 1;
  }
}
void AddRoundKey(uint8_t **s,uint8_t **Word,int Nb,int round)
{
  if(s==NULL)
    return;
  for (int j=0; j<Nb;j++)
  {
    s[0][j] = s[0][j]^Word[round*Nb+j][0];
    s[1][j] = s[1][j]^Word[round*Nb+j][1];
    s[2][j] = s[2][j]^Word[round*Nb+j][2];
    s[3][j] = s[3][j]^Word[round*Nb+j][3];
  }   
}

