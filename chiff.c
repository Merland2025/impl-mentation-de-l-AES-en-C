/* la fonction inverse de ShiftRows*/

void InvShiftRows(uint8_t **shift) 
{
  if (shift == NULL)
    return;
    
  uint8_t **t_cp = tab_cp(shift);
  if (t_cp == NULL)
    return;

  for (int i= 0; i<= 3; i++) 
    for (int j=0; j<= 3; j++) 
      shift[i][(i+j)%4] = t_cp[i][j];
  
  free_tab_4x4(t_cp);

}

/* la fonction inverse MixColumns*/

void InvMixColumns(uint8_t **tablo)
{
   if (tablo == NULL)
    return;
    
   uint8_t **mix = tab_cp(tablo);
   if (mix == NULL)
     return;
   
   uint8_t onze = 0xb;
   uint8_t treize = 0xd;
   uint8_t quatorze = 0xe;
   uint8_t neuf = 0x9;
   for (int i= 0; i<= 3; i++) 
    {
    tablo[0][i] = multiply(mix[0][i],quatorze) ^ multiply(mix[1][i],onze) ^ multiply(mix[2][i],treize) ^ multiply(mix[3][i],neuf) ;
    tablo[1][i] = multiply(mix[0][i],neuf) ^ multiply(mix[1][i],quatorze) ^ multiply(mix[2][i],onze) ^ multiply(mix[3][i],treize) ;
    tablo[2][i] = multiply(mix[0][i],treize) ^ multiply(mix[1][i],neuf) ^ multiply(mix[2][i],quatorze) ^ multiply(mix[3][i],onze) ;
    tablo[3][i] = multiply(mix[0][i],onze) ^ multiply(mix[1][i],treize) ^ multiply(mix[2][i],neuf) ^ multiply(mix[3][i],quatorze) ;
    }
     free_tab_4x4(mix);
}


/* fonction de chiffrement/déchiffrement */

void cipher(uint8_t *in, uint8_t *out, uint8_t **w, int Nb, int Nr) {
    if (in == NULL || out == NULL || w == NULL) 
        return;

    uint8_t **state = mem_tab_4x4();
    if (state == NULL) 
        return;

    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            state[j][i] = in[4 * i + j];
        }
    }

    int round = 0;

    AddRoundKey(state, w, Nb, round);

    for (round = 1; round < Nr; round++) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, w, Nb, round);
    }

    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, w, Nb, Nr);

    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            out[4 * i + j] = state[j][i];
        }
    }

    free_tab_4x4(state);
}


void Invcipher(uint8_t *in, uint8_t *out, uint8_t **w, int Nb, int Nr) {
    if (in == NULL || out == NULL || w == NULL) 
        return;

    uint8_t **state = mem_tab_4x4();
    if (state == NULL) 
        return;

    // Remplissage de l'état avec les données d'entrée
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < Nb; j++) {
            state[j][i] = in[Nb * i + j]; // Inversion de l'ordre des indices
        }
    }

    // Clé de dernière ronde additionnée
    AddRoundKey(state, w, Nb, Nr);

    // Boucle pour les rondes intermédiaires (inverse)
    for (int round = Nr - 1; round > 0; round--) { // Parcours inversé des rondes
        InvShiftRows(state);
        InvSubBytes(state);
        AddRoundKey(state, w, Nb, round);
        InvMixColumns(state);
    }

    // Dernières opérations sans InvMixColumns
    InvShiftRows(state);
    InvSubBytes(state);
    AddRoundKey(state, w, Nb, 0);

    // Copie de l'état dans le tableau de sortie
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < Nb; j++) { // Nb plutôt que 4
            out[Nb * i + j] = state[j][i]; // Inversion de l'ordre des indices
        }
    }

    free_tab_4x4(state);
}






















