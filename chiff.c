// Allocation de la mémoire pour un tableau 4x4

uint8_t **mem_tab_4x4() {
    uint8_t **tab = calloc(4, sizeof(uint8_t *));
    if (tab == NULL) 
        return NULL;

    for (int i = 0; i < 4; i++) {
        tab[i] = calloc(4, sizeof(uint8_t));
        if (tab[i] == NULL) return NULL;
    }
    return tab;
}

/* fonction pour liberer la mémoire*/

uint8_t **tab_cp(uint8_t **t)
{
  if (!t)
    return NULL;
    
  uint8_t **t_cp = mem_tab_4x4();
  if (!t_cp)
    return NULL;
  
  for (int i=0; i<4; i++)
    for (int j=0; j<4; j++)
      t_cp[i][j]=t[i][j];
    
  return t_cp;
  
}

void free_tab_4x4(uint8_t **tab) {
    if (tab == NULL) 
        return;

    for (int i = 0; i < 4; i++) {
        free(tab[i]);
    }
    free(tab);
}

/* on va écrire les quatre fonctions de l'AES */

void SubBytes(uint8_t **c)
{ 
  if(c==NULL)
    return;
    
  for (int i= 0; i<= 3; i++) 
    for (int j=0; j<= 3; j++) 
        c[i][j]=S_BOX[c[i][j]];
}


void ShiftRows(uint8_t **Shift) 
{
  if (Shift == NULL)
    return;
    
  uint8_t **t_cp = tab_cp(Shift);
  if (t_cp == NULL)
    return;

  for (int i= 0; i<= 3; i++) 
    for (int j=0; j<= 3; j++) 
      Shift[i][j] = t_cp[i][(j+i)%4];
  
  free_tab_4x4(t_cp);

}

uint8_t multiply(uint8_t a,uint8_t b) 
{
  if (a==0 || b==0)
    return 0;
  int p = (TAB_EXP[a] + TAB_EXP[b]) % 255;
  uint8_t c = TAB_HEX[p];
  
  return c;
}

void MixColumns(uint8_t **tablo)
{
   if (tablo == NULL)
    return;
    
   uint8_t **mix = tab_cp(tablo);
   if (mix == NULL)
     return;
   
   uint8_t deux = 0x02;
   uint8_t trois = 0x03;
   for (int i= 0; i<= 3; i++) 
    {
    tablo[0][i] = multiply(deux, mix[0][i]) ^ multiply(trois,mix[1][i]) ^ mix[2][i] ^ mix[3][i] ;
    tablo[1][i] = mix[0][i] ^ multiply(deux,mix[1][i]) ^ multiply(mix[2][i],trois) ^ mix[3][i];
    tablo[2][i] = mix[0][i] ^ mix[1][i] ^ multiply(deux,mix[2][i]) ^ multiply(trois,mix[3][i]);
    tablo[3][i] = multiply(mix[0][i],trois) ^ mix[1][i] ^ mix[2][i] ^ multiply(deux,mix[3][i]);
    }
}

void SubWord(uint8_t *Word)
{
  if (Word == NULL)
    return;

  for (int i=0; i<=3; i++)
    Word[i] = S_BOX[Word[i]];
}

